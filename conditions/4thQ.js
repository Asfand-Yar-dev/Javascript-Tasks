let Marks = prompt("Enter your marks: ");
if(Marks>=90 && Marks<=100){
    console.log("Grade A+");
}
else if(Marks>=80 && Marks<90){
    console.log("Grade B");
}
else if(Marks>=70 && Marks<80){
    console.log("Grade C");
}
else if(Marks>=60 && Marks<70){
    console.log("Grade D");
}
else if(Marks>=50 && Marks<60){
    console.log("Grade E");
}